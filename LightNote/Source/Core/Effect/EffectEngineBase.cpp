	{
	}

	//----------------------------------------------------------------------
	//
	//----------------------------------------------------------------------
	void EffectEngineBase::initialize( Manager* manager, int cacheSize )
	{
		mManager = manager;
		mEffectCoreCache.initialize( cacheSize, 0 );
	}

	//----------------------------------------------------------------------
	//
	//----------------------------------------------------------------------
	void EffectEngineBase::finalize()
	{
		mEffectCoreCache.finalize();
	}

	//----------------------------------------------------------------------
	//
	//----------------------------------------------------------------------
	Base::ICacheObject* EffectEngineBase::findEffectCoreCache( const lnSharingKey& key )
	{
		return mEffectCoreCache.findCacheAddRef( key );
	}

	//----------------------------------------------------------------------
	//
	//----------------------------------------------------------------------
	void EffectEngineBase::clearEffectCoreCache()
	{
		mEffectCoreCache.clearCacheList();
	}

