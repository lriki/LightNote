    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    GameObject::GameObject()
        : mDestroyed    (false)
    {
    }

    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    GameObject::~GameObject()
    {
        ln_foreach( Component* c, mComponentArray )
        {
            c->release();
        }
        mComponentArray.clear();
    }

    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    void GameObject::initialize()
    {
    }

    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    void GameObject::addComponent( Component* component )
    {
        if ( component )
        {
            mComponentArray.push_back( component );
            component->addRef();
        }
    }


